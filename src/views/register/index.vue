<script>
import { isvalidloginParam } from '@/utils/validate'
import { Message } from 'element-ui'

export default {
  data() {
    const validateLogintParam = (rule, value, callback) => {
      if (!isvalidloginParam(value)) {
        callback(new Error('Please enter the correct user name'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      registerForm: {
        registerParam: 'wangwenhai',
        password: 'wangwenhai'
      },
      registerRules: {
        registerParam: [
          { required: true, trigger: 'blur', validator: validateLogintParam }
        ],
        password: [
          { required: true, trigger: 'blur', validator: validatePassword }
        ]
      },
      passwordType: 'password',
      loading: false,
      showDialog: false,
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  created() {
    // window.addEventListener('hashchange', this.afterQRScan)
  },
  destroyed() {
    // window.removeEventListener('hashchange', this.afterQRScan)
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
    },
    handleregister() {
      this.$refs.registerForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store
            .dispatch('registerByregisterParam', this.registerForm)
            .then(response => {
              this.loading = false
              const data = response.data
              console.log(data.state)
              if (data.state === 1) {
                this.loading = false
                this.$router.push({ path: this.redirect || '/' })
              } else {
                Message.error('用戶名或密码错误')
                return false
              }
            })
            .catch(() => {
              this.loading = false
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    afterQRScan() {
      // const hash = window.location.hash.slice(1)
